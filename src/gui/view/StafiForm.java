     private void tabelaSelectedIndexChange() {
        final ListSelectionModel rowSM = TabelaStafi.getSelectionModel();
        rowSM.addListSelectionListener(new ListSelectionListener() {
            public void valueChanged(ListSelectionEvent Ise) {
                if (Ise.getValueIsAdjusting()) {
                    return;
                }
                ListSelectionModel rowSM = (ListSelectionModel) Ise.getSource();
                int selectedIndex = rowSM.getAnchorSelectionIndex();
                if (selectedIndex > -1) {
                    LogIn d = dtm.getLogIn(selectedIndex);
                    
                    idField.setText(Integer.toString(d.getLoginID()));
                    UserField.setText(d.getUserName());
                    PasswordField.setText(d.getPassword());
                   roliComboBox.setSelectedItem(d.getIDRoli());
                }
            }
        });
    }
     
    private void shtoButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_shtoButtonActionPerformed
        // TODO add your handling code here:
         int row = TabelaStafi.getSelectedRow();
        try {
            if (row == -1) {
                LogIn p = new LogIn();
                //p.setId(Integer.parseInt(idTxt.getText()));
                p.setLoginID(Integer.parseInt(idField.getText()));
                p.setUserName(UserField.getText());
                p.setPassword(PasswordField.getText());
                p.setIDRoli((Roli) roliComboBox.getSelectedItem());
            
                dr.create(p);
            } else {
                LogIn p = dtm.getLogIn(row);
                p.setLoginID(Integer.parseInt(idField.getText()));
                p.setUserName(UserField.getText());
                p.setPassword(PasswordField.getText());
                p.setIDRoli((Roli) roliComboBox.getSelectedItem());
               
                dr.edit(p);
            }
            clear();
            loadList();
        } catch (ProjectException ex) {
            JOptionPane.showMessageDialog(this, "Mesazhi:" + ex.getMessage());
        }
    }//GEN-LAST:event_shtoButtonActionPerformed

