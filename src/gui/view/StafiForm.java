    private List<Roli> roli;
    
    public void loadLlojiDhomesComboBox() {
        try {
            roli = rr.findAll();
            RoliComboBoxModel ldcb = new RoliComboBoxModel(roli);
            roliComboBox.setModel(ldcb);
            roliComboBox.repaint();
        } catch (ProjectException ex) {
            Logger.getLogger(DhomaForm.class.getName()).log(Level.SEVERE, null, ex);
        }
    }   
    
       public void clear() {
        TabelaStafi.clearSelection();
        idField.setText("");
        UserField.setText("");
        PasswordField.setText("");
        roliComboBox.setSelectedItem(null);
        
    }
         public void loadList() {
        try {
            dtm.addList(dr.findAll());
            TabelaStafi.setModel(dtm);
            dtm.fireTableDataChanged();

        } catch (ProjectException ex) {
            Logger.getLogger(NrKatitForm.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

